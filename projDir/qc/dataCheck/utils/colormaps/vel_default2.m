%Default colormap for reflectivity values

function x = zdr_default(n);


if nargin==1 & isempty(n)
    n = size(get(gcf,'Colormap'),1);
end;

cmap = [0,0,0;...
  72,61,139;...
  0,0,128;...
  0,0,255;...
  30,144,255;...
  0,191,255;...
  64,224,208;...
  60,179,113;...
  34,139,34;...
  107,142,35;...
  154,205,50;...
  105,105,105;...
  112,128,144;...
  255,255,0;...
  255,215,0;...
  255,165,0;...
  255,127,80;...
  255,0,0;...
  199,21,133;...
  255,20,147;...
  218,112,214;...
  221,160,221;...
  255,182,193;...
  0,0,0 ...
    ];

cmap=cmap./255;

if nargin < 1
    n = size(cmap,1);
end;

x = interp1(linspace(0,1,size(cmap,1)),cmap(:,1),linspace(0,1,n)','linear');
x(:,2) = interp1(linspace(0,1,size(cmap,1)),cmap(:,2),linspace(0,1,n)','linear');
x(:,3) = interp1(linspace(0,1,size(cmap,1)),cmap(:,3),linspace(0,1,n)','linear');

x = min(x,1);
x = max(x,0);